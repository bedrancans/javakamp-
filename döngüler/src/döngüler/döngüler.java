package döngüler;

public class döngüler {
	public static void main(String[] args) {
		// for döngüsü
		
		for(int i=1;i<10;i++) {
					System.out.println(i);
				 }
			System.out.println("for bitti");
		//while döngüsü
		int i= 1;
		while(i<10) {
			System.out.println(i);
			i++;
		}
		System.out.println("While bitti");
		//Do-While
		int j=1;
		do {
			System.out.println(j);
			j= j+1;
		}while(j<10);
		System.out.println("Do-While bitti");
		
	}

}


